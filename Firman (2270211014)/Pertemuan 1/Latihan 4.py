#tipe data Boolean
print(True)

#tipe data String
print("ayo belajar python")
print('belajar python sangat mudah')

#tipe data Integer
print(25)

#tipe data Float
print(3.20)

#tipe data Hexadecimal
#print(9a)

#tipe data Complex
print(5j)

#tipe data List
print([10,20,30,40,50])
print(["sepuluh", "dua puluh", "tiga puluh"])

#tipe data Tuple
print((1,2,3,4,5))
print(("satu", "dua", "tiga"))

#tipe data Dictionary
print({"nama":"Firman", 'umur':18})

#tipe data Dictionary dimasukan ke dalam variabel biodata
biodata = {"nama":"Udin", 'umur':21} #proses inisialisasi variabel biodata
print(biodata) #proses pencetakan variabel biodata yang berisi tipe data Dictionary
print(type(biodata)) #fungsi untuk mengecek jenis tipe data. akan tampil <class 'dict'> yang
#berarti dict adalah tipe data dictionary